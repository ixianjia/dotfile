-- ============================================================
-- Neovim 全局快捷键映射
-- ============================================================
-- 本文件定义全局快捷键，包括窗口导航、文本移动、搜索、Buffer 管理等
-- Leader 键为 <Space>
-- ============================================================

local keymap = vim.keymap.set
local opts = { noremap = true, silent = true }

-- ============================================================
-- 窗口导航 (Ctrl + 方向键)
-- ============================================================
-- 使用 Ctrl+h/j/k/l 在分割窗口间切换焦点
keymap("n", "<C-h>", "<C-w>h", opts)
keymap("n", "<C-j>", "<C-w>j", opts)
keymap("n", "<C-k>", "<C-w>k", opts)
keymap("n", "<C-l>", "<C-w>l", opts)

-- ============================================================
-- 窗口大小调整 (Ctrl + 箭头键)
-- ============================================================
-- 使用方向键调整当前窗口的高度和宽度
keymap("n", "<C-Up>", ":resize -2<CR>", opts)
keymap("n", "<C-Down>", ":resize +2<CR>", opts)
keymap("n", "<C-Left>", ":vertical resize -2<CR>", opts)
keymap("n", "<C-Right>", ":vertical resize +2<CR>", opts)

-- ============================================================
-- 文本块上下移动 (Alt + j/k)
-- ============================================================
-- 在可视模式下选中多行后，使用 Alt+j/k 上下移动
keymap("v", "<A-j>", ":m '>+1<CR>gv=gv", opts)
keymap("v", "<A-k>", ":m '<-2<CR>gv=gv", opts)

-- ============================================================
-- 可视模式下缩进保持选中
-- ============================================================
-- 使用 </> 缩进后仍保持选中状态，方便连续操作
keymap("v", "<", "<gv")
keymap("v", ">", ">gv")

-- ============================================================
-- 清除搜索高亮
-- ============================================================
-- 使用 <leader>h 清除搜索结果的黄色高亮
keymap("n", "<leader>h", "<cmd>nohlsearch<CR>", opts)

-- ============================================================
-- 快速保存
-- ============================================================
-- 使用 Ctrl+s 保存文件（支持普通模式和插入模式）
keymap("n", "<C-s>", "<cmd>w<CR>", opts)
keymap("i", "<C-s>", "<ESC><cmd>w<CR>", opts)

-- ============================================================
-- 快速退出
-- ============================================================
-- 使用 Ctrl+q 退出当前窗口
keymap("n", "<C-q>", "<cmd>q<CR>", opts)

-- ============================================================
-- Buffer 切换
-- ============================================================
-- Shift+H/L 切换上一个/下一个 Buffer
-- <leader>x 关闭当前 Buffer
keymap("n", "<S-l>", "<cmd>bnext<CR>", opts)
keymap("n", "<S-h>", "<cmd>bprevious<CR>", opts)
keymap("n", "<leader>x", "<cmd>bdelete<CR>", opts)

-- ============================================================
-- 诊断信息导航
-- ============================================================
-- <leader>cd 显示当前行诊断浮窗
-- [d / ]d 跳转到上一个/下一个诊断位置
-- <leader>q 将诊断信息加入 Location List
keymap("n", "<leader>cd", vim.diagnostic.open_float, opts)
keymap("n", "[d", vim.diagnostic.goto_prev, opts)
keymap("n", "]d", vim.diagnostic.goto_next, opts)
keymap("n", "<leader>q", vim.diagnostic.setloclist, opts)

-- ============================================================
-- 终端相关
-- ============================================================
-- <leader>tt 打开内置终端
-- ESC 退出终端插入模式
keymap("n", "<leader>tt", "<cmd>terminal<CR>", opts)
keymap("t", "<ESC>", "<C-\\><C-n>", opts)

-- ============================================================
-- 切换相对行号
-- ============================================================
-- <leader>tn 切换相对行号显示
keymap("n", "<leader>tn", "<cmd>set relativenumber!<CR>", opts)

-- ============================================================
-- 粘贴时保留被替换内容
-- ============================================================
-- 在可视模式下粘贴时，将被替换的文本保存到寄存器，而不是覆盖
keymap("x", "p", "p:let @+=@0<CR>:let @\"=@0<CR>", opts)
