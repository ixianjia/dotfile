-- ============================================================
-- Neovim 编辑器选项配置
-- ============================================================
-- 本文件包含 Neovim 的核心编辑器设置，包括行号、缩进、搜索、剪贴板等
-- ============================================================

local opt = vim.opt

-- ============================================================
-- 行号显示
-- ============================================================
opt.number = true       -- 显示绝对行号
opt.relativenumber = true  -- 显示相对行号（方便跳转）

-- ============================================================
-- 制表符与缩进
-- ============================================================
opt.tabstop = 4         -- Tab 字符显示的宽度（4 个空格）
opt.shiftwidth = 4      -- 自动缩进的宽度（>> 或 << 命令）
opt.expandtab = true    -- 将 Tab 转换为空格
opt.autoindent = true   -- 新行继承上一行的缩进
opt.smartindent = true  -- 智能缩进（识别代码结构）

-- ============================================================
-- 换行设置
-- ============================================================
opt.wrap = false        -- 不自动换行（超出屏幕时横向滚动）

-- ============================================================
-- 搜索设置
-- ============================================================
opt.ignorecase = true   -- 搜索时忽略大小写
opt.smartcase = true    -- 搜索词包含大写字母时区分大小写
opt.hlsearch = false    -- 不高亮搜索结果
opt.incsearch = true    -- 输入搜索词时实时预览匹配位置

-- ============================================================
-- 光标行高亮
-- ============================================================
opt.cursorline = true   -- 高亮当前光标所在行

-- ============================================================
-- 窗口分割
-- ============================================================
opt.splitright = true   -- 垂直分割窗口在右侧打开
opt.splitbelow = true   -- 水平分割窗口在下方打开

-- ============================================================
-- 退格键行为
-- ============================================================
-- 允许退格键删除：缩进、行尾换行、插入起始位置
opt.backspace = "indent,eol,start"

-- ============================================================
-- 剪贴板集成
-- ============================================================
-- 使用系统剪贴板（可与系统其他应用复制粘贴）
opt.clipboard = "unnamedplus"

-- ============================================================
-- 撤销功能
-- ============================================================
opt.undofile = true     -- 启用持久化撤销（关闭文件后仍可撤销）

-- ============================================================
-- 文件编码
-- ============================================================
opt.fileencoding = "utf-8"  -- 默认使用 UTF-8 编码

-- ============================================================
-- 界面显示
-- ============================================================
opt.showmode = false    -- 不在底部显示当前模式（如 -- INSERT --）
opt.cmdheight = 1       -- 命令行高度为 1 行
opt.signcolumn = "yes"  -- 始终显示符号列（用于 LSP 诊断、Git 状态等）

-- ============================================================
-- 折叠设置
-- ============================================================
opt.foldmethod = "manual"  -- 手动折叠（使用 zc/zo 等命令）
opt.foldenable = false     -- 默认不启用折叠

-- ============================================================
-- 超时设置
-- ============================================================
opt.timeoutlen = 300    -- 快捷键序列超时时间（毫秒）
opt.updatetime = 250    -- CursorHold 事件触发间隔（毫秒）

-- ============================================================
-- 滚动偏移
-- ============================================================
-- 光标距离屏幕顶部/底部至少保留 8 行
opt.scrolloff = 8

-- ============================================================
-- 鼠标支持
-- ============================================================
opt.mouse = "a"         -- 在所有模式下启用鼠标

-- ============================================================
-- 真彩色支持
-- ============================================================
opt.termguicolors = true  -- 启用 24 位真彩色（主题显示更准确）
